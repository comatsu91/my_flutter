class UserData {
  final String nama;
  final String jabatan;
  final String jurusan;
  final String email;
  final String username;
  final String password;

  UserData({
    required this.nama,
    required this.jabatan,
    required this.jurusan,
    required this.email,
    required this.username,
    required this.password,
  });
}
